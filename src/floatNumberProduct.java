import java.lang.*;


public class floatNumberProduct {
	public static void main(String args[]) {
		
		float num1 = 3.25f; //f for ensuring floating point number
		float num2 = 1.25f;
		
		float result = num1*num2;
		
		System.out.println("The product result is : "+result);
	}
}
